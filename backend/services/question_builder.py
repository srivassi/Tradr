# ─── Context builders ─────────────────────────────────────────────────────────

async def _tradr_context(market: str) -> str:
    from services.market_service import get_quote
    from services.news_service import get_headlines

    tickers = {
        "india": [("^NSEI", "Nifty 50"), ("^BSESN", "Sensex"), ("^NSEBANK", "Bank Nifty")],
        "us":    [("^GSPC", "S&P 500"), ("^IXIC", "NASDAQ"), ("^DJI", "Dow Jones")],
        "eu":    [("^GDAXI", "DAX"), ("^FCHI", "CAC 40")],
    }.get(market, [])

    moves: list[str] = []
    for ticker, name in tickers:
        try:
            q = await asyncio.to_thread(get_quote, ticker)
            d = "up" if q["change_pct"] >= 0 else "down"
            moves.append(f"{name} {d} {abs(q['change_pct']):.1f}% at {q['price']:.0f}")
        except Exception:
            pass

    headlines = await get_headlines(market, page_size=3)
    hl = "; ".join(h["title"] for h in headlines[:3]) if headlines else "no headlines"
    lines = [f"Date: {date.today().isoformat()}"]
    if moves:
        lines.append("Markets today: " + ", ".join(moves))
    lines.append(f"News: {hl}")
    return "\n".join(lines)


# ─── Builders ────────────────────────────────────────────────────────────────

async def build_tradr_questions(market: str, count: int = 5) -> list[dict]:
    from services.claude_service import generate_question_variant
    context = await _tradr_context(market)
    topics = _TRADR_TOPICS.get(market, _TRADR_TOPICS["india"])
    results: list[dict] = []

    for i in range(count):
        topic = topics[i % len(topics)]
        difficulty = (i % 3) + 2
        try:
            data = await generate_question_variant(
                track="tradr",
                topic=topic,
                market_context=context,
                difficulty=difficulty,
            )
            seed = f"tradr-{market}-{date.today().isoformat()}-{i}"
            results.append({
                "id":                hashlib.md5(seed.encode()).hexdigest()[:12],
                "lesson_id":         f"live-{market}-{i % len(topics)}",
                "track":             "tradr",
                "market":            market,
                "type":              "mcq",
                "question":          data["question"],
                "options":           json.dumps(data["options"]),
                "correct":           data["correct"],
                "explanation_short": data.get("explanation_short", ""),
                "tags":              data.get("tags", []),
                "difficulty":        difficulty,
                "active":            True,
                "generated_at":      datetime.now(timezone.utc).isoformat(),
            })
        except Exception as exc:
            logger.error("Tradr question build failed (%s, i=%d): %s", market, i, exc)

    return results


