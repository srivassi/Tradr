async def store_questions(questions: list[dict]) -> None:
    from database.supabase_client import get_client
    if not questions:
        return
    db = get_client()
    db.table("questions").upsert(questions).execute()
    logger.info("Stored %d questions in Supabase", len(questions))


async def run_weekly_question_build() -> None:
    """Entry point called by APScheduler alongside the scenario build."""
    logger.info("Weekly question build starting")
    all_questions: list[dict] = []

    for market in ("india", "us", "eu"):
        try:
            qs = await build_tradr_questions(market, count=5)
            all_questions.extend(qs)
        except Exception as exc:
            logger.error("Tradr question build failed for %s: %s", market, exc)

    try:
        qs = await build_codr_questions(count=8)
        all_questions.extend(qs)
    except Exception as exc:
        logger.error("Codr question build failed: %s", exc)

    await store_questions(all_questions)
    logger.info("Weekly question build complete — %d questions stored", len(all_questions))
